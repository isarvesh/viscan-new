                <h3 style="color: #4b0859;">Lateral Cephalogram</h3>
                <p>A Lateral Cephalogram (or Lat Ceph) is an x-ray taken of the side of the face with very precise positioning so that various measurements can be made to determine the current and future relationship of the top and bottom jaw (maxilla and mandible) and therefore assess the nature of a patient’s bite. This is particularly useful to plan any orthodontic treatment that may be necessary. </p>
