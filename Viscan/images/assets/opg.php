                <p>The orthopantomogram (also known as an orthopantomograph, pantomogram, OPG or OPT) is a panoramic single image radiograph of the mandible, maxilla and teeth. It is often encountered in dental practice and occasionally in the emergency department; providing a convenient, inexpensive and rapid way to evaluate the gross anatomy of the jaws and related pathology. </p>
