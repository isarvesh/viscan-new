                        <p>CBCT stands for Cone Beam Computed Tomography. It is a technology used to take three dimensional (3-D) images of your teeth, maxillary sinus, nerve pathways, and bone in the maxillofacial region with a single scan. The CBCT system rotates around the patient in approximately 10-30 seconds, capturing data using a cone-shaped x-ray beam.</p>
                        <p>Dental cone beam computed tomography is used when regular two dimensional dental x-rays are not sufficient. With CBCT, clinicians can get highly detailed 3-D views of the facial region with 6-15 times lower radiation exposure than a conventional CT scan. This may help with the diagnosis, treatment planning and evaluation of certain conditions.</p>
