                        <h3 style="color: #4b0859;">CBCT In Implants</h3>
                        <p>Study consists of cross sections and panoramic projections using Cone Beam CT (CBCT) scanner. It can be of an individual area or the whole mandible or maxilla. CBCT 3D technology is one of the most effective tools available for evaluating implant sites. 3D images can identify potential pathologies and structural abnormalities with unprecedented accuracy.</p>
                        <p>Cross sectional and panoramic views facilitate various measurements such as: height and width of the implant sites, mandibular edentulous site, a potential implant site near the mental foremen, width of the buccal/lingual ridge and cortical bone density. 3D images highlight the cortical bone thickness, the cancellous bone density, inferior alveolar nerve and the mental foremen location. They also influence the choice of the appropriate implant to be used, its placement, its width and consideration of “die back” from dense cortical bone.</p>
