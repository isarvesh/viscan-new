                        <h3 style="color: #4b0859;">Is there any special preparation required?</h3>
                        <p>There is no special preparation required and there are no after effects following the scan. Once at the practice, patients will be asked to remove any jewellery or metallic items from the head and neck region, including any piercings, hair accessories, dentures and plates, as these often interfere with x-ray image formation and produce unwanted artefacts.</p>
