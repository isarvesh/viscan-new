							<hr/>
						</div>
	  					    <p>A CBCT has quicker motion compared to the spiral motion of a traditional CT scanner. It is therefore possible to conduct a CT scan using a CBCT scanner with lower doses of radiation. An example can be found in panoramic radiography. </p> 
	  					    <p><strong style="color: #4b0859;font-weight: 900;font-size: 16px;">A typical CT scan will require separate maxilla and mandible scans which expose the patient to 200-300 times the required radiation for this radiography.</strong></p>
	                        <p>If both jaws are scanned, the exposure doubles! A CBCT scans both the maxilla and mandible at the same time hence reducing exposure.</p>  					
	  					    <p>CT has not been widely utilized due to the high radiation dosage, the cost of the machine and a dentist’s unfamiliarity with interpreting CT results.</p><p> <strong style="color: #4b0859;font-weight: 900;font-size: 16px;">The use of CBCT can negate these concerns and allow for improved diagnosis and patient safety. </strong></p>
